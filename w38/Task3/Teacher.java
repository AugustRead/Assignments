class Teacher {

String name;
int age;
boolean isFemale;

  public Teacher(String name, int age, boolean isFemale) {

    this.name = name;
    this.age = age;
    this.isFemale = isFemale;


  }

  public String getName() {
		return name;
	}
	public int getAge() {
		return age;
	}
	public boolean getIsFemale() {
		return isFemale;
	}
}
